#include "libft.h"

int	ft_isalnum(int c)
{
	int	result;

	result = 1;
	if (('a' <= c && c <= 'z')
		|| ('A' <= c && c <= 'Z')
		|| ('0' <= c && c <= '9'))
		result = 1;
	else
		return (0);
	return (result);
}
